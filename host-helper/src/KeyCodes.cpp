uint32_t AppWindow::toIpcKeyModifier(QKeyEvent *e) {
	uint32_t flags = 0;
	if ((e->modifiers() & Qt::NoModifier))
		flags |= IPC_NO_MODIFIER;
	if ((e->modifiers() & Qt::ShiftModifier))
		flags |= IPC_SHIFT_MODIFIER;
	if ((e->modifiers() & Qt::ControlModifier))
		flags |= IPC_CONTROL_MODIFIER;
	if ((e->modifiers() & Qt::AltModifier))
		flags |= IPC_ALT_MODIFIER;
	if ((e->modifiers() & Qt::MetaModifier))
		flags |= IPC_META_MODIFIER;
	if ((e->modifiers() & Qt::KeypadModifier))
		flags |= IPC_KEYPAD_MODIFIER;
	if ((e->modifiers() & Qt::GroupSwitchModifier))
		flags |= IPC_GROUP_SWITCH_MODIFIER;
	return flags;
}

